// 2. Create a java program that takes four valiable int,double,float and character and also there values
class data_type
{
	public static void main(String[] args)
	{
		int a = 10;
		double b = 20.23;
		float c = 3.14f;
		char d = 'A';
		System.out.println("Integer Value = "+a);
		System.out.println("Double Value = "+b);
		System.out.println("Float Value = "+c);
		System.out.println("Character Value = "+d);
	}
}
